    public void CalcularTotal() {
        total = 0;
        cantidad = 0;
        preciounitario = 0;
        cantidad = (Integer.parseInt(txtCantidad.getText()));
        preciounitario = Double.parseDouble(txtPrecioUnitario.getText());
        total = total + cantidad * preciounitario;
        txtValorTotal.setText("" + total);
    }

