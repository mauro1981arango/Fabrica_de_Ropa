        txtRefArticulo.setText(tblMateriaPrima.getValueAt(fila, 1).toString());
        cbxTipo.setSelectedItem(tblMateriaPrima.getValueAt(fila, 2).toString());
        cbxNombreEmpresa.setSelectedItem(tblMateriaPrima.getValueAt(fila, 3).toString());
