    private void tblNominaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNominaMouseClicked
        int fila = tblNomina.rowAtPoint(evt.getPoint());
        txtIdPago.setText(tblNomina.getValueAt(fila, 0).toString());
        cbxIdMes.setSelectedItem(tblNomina.getValueAt(fila, 1).toString());
        String IsFecha = String.valueOf(tblNomina.getValueAt(tblNomina.getSelectedRow(), 2));
        SimpleDateFormat formatoFecha = new SimpleDateFormat("yyyy-MM-dd");
        java.util.Date fecha;
        try {
            fecha = (java.util.Date) formatoFecha.parse(IsFecha);
            jDateFecha.setDate(fecha);
        } catch (ParseException e) {
            System.err.println("Error " + e.toString());
        }
        cbxIdRhumanos.setSelectedItem(tblNomina.getValueAt(fila, 3).toString());
        cbxIdSalario.setSelectedItem(tblNomina.getValueAt(fila, 4).toString());
    }//GEN-LAST:event_tblNominaMouseClicked

